            ],
        ];
    }

    public function getServiceConfig()
    {
        return [
            // Legacy Zend Framework aliases
            'aliases' => [
            ],
            'factories' => [
                'FooObject' => function ($sm) {
                    return new \stdClass();
                }
            ],
        ];
    }
}
